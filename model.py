def load_heart_dataset():
    df = pd.read_csv(io.StringIO(HEART_CSV))
    df = df.dropna()
    # Normalize target to binary
    df['target'] = (df['target'] >= 1).astype(int)
    return df

# ── Load & Prepare ─────────────────────────────────────────────────────────────
print("Loading real UCI Heart Disease Dataset (Cleveland)...")
df = load_heart_dataset()
print(f"Records: {len(df)} | Heart Disease cases: {df['target'].sum()} ({df['target'].mean()*100:.1f}%)")

FEATURE_COLS = ['age','sex','cp','trestbps','chol','fbs','restecg',
                'thalach','exang','oldpeak','slope','ca','thal']

X = df[FEATURE_COLS]
y = df['target']

scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)
X_train, X_test, y_train, y_test = train_test_split(
    X_scaled, y, test_size=0.2, random_state=42, stratify=y
)

# ── Train ──────────────────────────────────────────────────────────────────────
print("\nTraining Gradient Boosting Classifier on real heart data...")
model = GradientBoostingClassifier(n_estimators=150, learning_rate=0.1, max_depth=4, random_state=42)
model.fit(X_train, y_train)

y_pred = model.predict(X_test)
acc    = accuracy_score(y_test, y_pred)
cv     = cross_val_score(model, X_scaled, y, cv=5)
print(f"\n[OK] Test Accuracy:       {acc*100:.1f}%")
print(f"[OK] Cross-Val Accuracy:  {cv.mean()*100:.1f}% +/- {cv.std()*100:.1f}%")
print(f"\n{classification_report(y_test, y_pred, target_names=['No Heart Disease','Heart Disease'])}")

# ── Save ───────────────────────────────────────────────────────────────────────
with open("disease_model.pkl","wb") as f: pickle.dump(model, f)
with open("scaler.pkl","wb") as f:        pickle.dump(scaler, f)
with open("feature_cols.pkl","wb") as f:  pickle.dump(FEATURE_COLS, f)
print("[OK] Saved: disease_model.pkl | scaler.pkl | feature_cols.pkl")

# ── Utility Functions (used by app.py) ────────────────────────────────────────
def add_dp_noise(value, sensitivity=2.0, epsilon=0.5):
    return round(float(value) + np.random.laplace(0, sensitivity/epsilon), 2)

def get_feature_importance(mdl, names):
    imp    = mdl.feature_importances_
    result = {n: round(float(i/imp.sum())*100, 1) for n,i in zip(names, imp)}
    return dict(sorted(result.items(), key=lambda x: x[1], reverse=True))

def calculate_loan(income, credit_score, disease_risk_score, treatment_cost):
    score = 0
    if income >= 50000:    score += 40
    elif income >= 25000:  score += 25
    elif income >= 10000:  score += 10
    if credit_score >= 750:   score += 40
    elif credit_score >= 650: score += 25
    elif credit_score >= 550: score += 10
    if disease_risk_score >= 70:   score += 20  # urgent = priority
    elif disease_risk_score >= 40: score += 10
    approved  = score >= 40
    loan_amt  = min(income * 12, treatment_cost * 1.2) if approved else 0  # higher multiplier for heart
    r, n      = 0.085/12, 24  # 24 month tenure for larger amounts
    emi       = loan_amt * r*(1+r)**n / ((1+r)**n-1) if loan_amt > 0 else 0
    return {"approved":approved,"score":score,"loan_amount":round(loan_amt),
            "emi":round(emi),"tenure_months":n,"interest_rate":"8.5%"}

